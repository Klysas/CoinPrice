using Newtonsoft.Json;

namespace CoinPrice
{
	public class CoinJsonItem
	{
		public string id { get; set; }
		public string name { get; set; }
		public string symbol { get; set; }
		public string rank { get; set; }
		public string price_usd { get; set; }
		public string price_eur { get; set; }
		[JsonProperty(PropertyName = "24h_volume_usd")]   //since in c# variable names cannot begin with a number, you will need to use an alternate name to deserialize
		public string volume_usd_24h { get; set; }
		[JsonProperty(PropertyName = "24h_volume_eur")]   //since in c# variable names cannot begin with a number, you will need to use an alternate name to deserialize
		public string volume_eur_24h { get; set; }
		public string market_cap_usd { get; set; }
		public string market_cap_eur { get; set; }
		public string available_supply { get; set; }
		public string total_supply { get; set; }
		public string percent_change_1h { get; set; }
		public string percent_change_24h { get; set; }
		public string percent_change_7d { get; set; }
		public string last_updated { get; set; }
	}
}
